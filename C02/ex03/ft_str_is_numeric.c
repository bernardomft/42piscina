#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int		i;
	int		num;

	num = 1;
	i = 0;
	if (str[0] == '\0')
		return (num);
	while (str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
			num = 0;
		i++;
	}
	return (num);
}
