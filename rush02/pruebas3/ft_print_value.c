#include <unistd.h>

void	ft_print_value(char *str)
{
	int i;

	i = 0;
	while (str[i] != ' ')
		i++;
	i++;
	while (str[i] != '\n')
	{
		write(1, &str[i], 1);
		i++;
	}
}
