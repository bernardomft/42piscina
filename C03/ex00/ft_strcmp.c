#include <unistd.h>

int			ft_strcmp(char *s1, char *s2)
{
	int		i;
	int		output;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			output = (s1[i] - s2[i]);
			return (output);
		}
		i++;
	}
	return (0);
}
